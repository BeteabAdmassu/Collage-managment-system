    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        if(newusername.getText().length() == 0 || newpassword.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please fill the entire form");
            return;
        }
        if(Util.isNumeric(username.getText())){
            JOptionPane.showMessageDialog(this, "Username can not have numbers");
            return;
        }
        
        if(newusername.getText().length() > 10 || newpassword.getText().length() > 20){
            JOptionPane.showMessageDialog(this, "Username can have 10 characters maximum, and password can have 20 characters maximum.");
            return;
        }
        
        String uname = newusername.getText();
        String pass = newpassword.getText();
        boolean ad = isAdmin.isSelected();
        try{
            PreparedStatement s = con.prepareStatement("insert into users values(?,?,?)");
            s.setString(1, uname);
            s.setString(2,pass);
            s.setBoolean(3, ad);
            s.executeUpdate();
        }catch(SQLException s){
           JOptionPane.showMessageDialog(this, "Sorry Try Again Later");
           s.printStackTrace();
           CardLayout c = (CardLayout) container.getLayout();
           c.show(container, "home");
        }
       
        
//        JOptionPane.showMessageDialog(this, "Successfully registered the new user");
        newusername.setText("");
        newpassword.setText("");
        isAdmin.setSelected(false);
    }//GEN-LAST:event_jButton11ActionPerformed

